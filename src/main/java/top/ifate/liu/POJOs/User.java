package top.ifate.liu.pojo;


public class User {

  private long uid;
  private String uname;
  private String upwd;
  private String urole;
  private String upression;


  public long getUid() {
    return uid;
  }

  public void setUid(long uid) {
    this.uid = uid;
  }


  public String getUname() {
    return uname;
  }

  public void setUname(String uname) {
    this.uname = uname;
  }


  public String getUpwd() {
    return upwd;
  }

  public void setUpwd(String upwd) {
    this.upwd = upwd;
  }


  public String getUrole() {
    return urole;
  }

  public void setUrole(String urole) {
    this.urole = urole;
  }


  public String getUpression() {
    return upression;
  }

  public void setUpression(String upression) {
    this.upression = upression;
  }

}
